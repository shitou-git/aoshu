// ========== 生成 ==========
function makeHints(list) {
  return list.map(p => hintFor(p.title, p.text));
}

const hintsData = {
  easy: makeHints(problems.easy),
  medium: makeHints(problems.medium),
  hard: makeHints(problems.hard),
};

// 双检：确保没有出现「答案是…」这种泄露
function esc(s) {
  // 替换双引号与反斜杠
  return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

function buildJS(obj) {
  const lines = [];
  lines.push('const hintsData = {');
  for (const level of ['easy', 'medium', 'hard']) {
    lines.push(`  ${level}: [`);
    for (const arr of obj[level]) {
      const items = arr.map(s => `"${esc(s)}"`).join(', ');
      lines.push(`    [${items}],`);
    }
    lines.push('  ],');
  }
  lines.push('};');
  lines.push('');
  lines.push('module.exports = hintsData;');
  return lines.join('\n');
}

